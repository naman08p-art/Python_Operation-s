tuple1 = (1, 2, 3)
tuple2 = (4, 5, 6)

# Nested Tuple
nested_tuple = ("Hello", (1,2,3), ("Python", "Programming"))
print("Nested Tuple:", nested_tuple[1], nested_tuple[2])

# Repetition of Tuple
repeat_tuple = ("Repeat",)*3
print("Repeated Tuple:", repeat_tuple)

# Concatenation of Tuple
print("Concatenation:", tuple1 + tuple2)
